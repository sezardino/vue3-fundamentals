<template>
  <label v-if="label">{{ label }}</label>

  <input
    v-bind="$attrs"
    :type="$attrs.type || 'text'"
    :value="modelValue"
    @input="$emit('update:modelValue', $event.target.value)"
  />
</template>

<script>
export default {
  name: "BaseInput",
  props: {
    label: {
      type: String,
      default: "",
    },
    modelValue: {
      type: [String, Number],
      default: "",
    },
  },
};
</script>
